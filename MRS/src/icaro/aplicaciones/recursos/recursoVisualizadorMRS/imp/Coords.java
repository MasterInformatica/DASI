package icaro.aplicaciones.recursos.recursoVisualizadorMRS.imp;

public class Coords {
	private int x;
	private int y;
	public Coords(int xx, int yy){
		x = xx;
		y = yy;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
	
}
